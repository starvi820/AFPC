package one.auditfinder.sever.vo.version;

public class FlashPlayerVo {
	
	private int flashId;
	private String flashPlatform;
	private String flashBrowser;
	private String flashVersion;
	public int getFlashId() {
		return flashId;
	}
	public void setFlashId(int flashId) {
		this.flashId = flashId;
	}
	public String getFlashPlatform() {
		return flashPlatform;
	}
	public void setFlashPlatform(String flashPlatform) {
		this.flashPlatform = flashPlatform;
	}
	public String getFlashBrowser() {
		return flashBrowser;
	}
	public void setFlashBrowser(String flashBrowser) {
		this.flashBrowser = flashBrowser;
	}
	public String getFlashVersion() {
		return flashVersion;
	}
	public void setFlashVersion(String flashVersion) {
		this.flashVersion = flashVersion;
	}
	
	

}
