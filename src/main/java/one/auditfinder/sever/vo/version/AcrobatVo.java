package one.auditfinder.sever.vo.version;

public class AcrobatVo {
	
	private int acrobatId;
	private String acrobatVersion;
	private String acrobatName;
	private String acrobatSize;
	private String acrobatDate;
	public int getAcrobatId() {
		return acrobatId;
	}
	public void setAcrobatId(int acrobatId) {
		this.acrobatId = acrobatId;
	}
	public String getAcrobatVersion() {
		return acrobatVersion;
	}
	public void setAcrobatVersion(String acrobatVersion) {
		this.acrobatVersion = acrobatVersion;
	}
	public String getAcrobatName() {
		return acrobatName;
	}
	public void setAcrobatName(String acrobatName) {
		this.acrobatName = acrobatName;
	}
	public String getAcrobatSize() {
		return acrobatSize;
	}
	public void setAcrobatSize(String acrobatSize) {
		this.acrobatSize = acrobatSize;
	}
	public String getAcrobatDate() {
		return acrobatDate;
	}
	public void setAcrobatDate(String acrobatDate) {
		this.acrobatDate = acrobatDate;
	}
	
	
	

}
